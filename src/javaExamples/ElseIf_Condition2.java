package javaExamples;

public class ElseIf_Condition2 {
	public static void main(String args[]) {
		int a=10;
		if (a<=10) {
			System.out.println("a value is 10");
		}
		else if(a>=15) {
			System.out.println("a value is 15");
		}
		else if(a==10) {
			System.out.println("a value is 10");
		}
	}

}
