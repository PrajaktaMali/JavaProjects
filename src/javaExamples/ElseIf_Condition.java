package javaExamples;

public class ElseIf_Condition {
	public static void main(String args[]) {
	int a=50;
	if(a==10) {
		System.out.println("a value is 10");
	}
	else if(a==20) {
		System.out.println("a value is 20");
	}
	else if(a==30) {
		System.out.println("a value is 30");
	}
	else if(a==40) {
		System.out.println("a value is 40");
	}
	else if(a==50) {
		System.out.println("a value is 50");
	}
	}

}