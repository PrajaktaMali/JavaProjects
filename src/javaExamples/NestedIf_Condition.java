package javaExamples;

public class NestedIf_Condition {
public static void main(String args[]) {
	int a=20;
	int b=30;
	if(a==20) {
		if(b==30) {
			System.out.println("a value is 20 & b value is 30");
			}
			else {
				System.out.println("else statement of 2nd if condition");
		}
		
		}
		else {
			System.out.println("else statement of 1st if condition");
	}
}
}
